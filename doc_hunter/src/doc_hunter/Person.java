package doc_hunter;

public class Person {
    protected String id;
    protected String nama;
    protected String alamat;
    protected String nomorHP;
	protected String email;
	protected String password;

    
    public Person(String id, String nama, String alamat, String nomorHP, String email, String password) {
        this.id = id;
        this.nama = nama;
        this.alamat = alamat;
        this.nomorHP = nomorHP;
        this.email = email;
        this.password = password;
    }
    // getters and setters
}



